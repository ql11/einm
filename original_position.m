function IF = original_position(P0)
%判断电子的初始位置是否在求解位置，目的是结合对称性，减少计算时间
%内环半径之外
    F_0 = (P0(1)^2 + P0(2)^2) >= (25e-3)^2;

    %外环半径之内
    F_1 = (P0(1)^2 + P0(2)^2) <= (151e-3)^2;

    %角度范围大于0°
    F_2 = P0(2) >= 0;

    %角度范围小于22.5°
    F_3 = P0(1) > 0 | P0(2)/P0(1) <= tan(pi/8);


    IF = F_0 & F_1 & F_2 & F_3;
end